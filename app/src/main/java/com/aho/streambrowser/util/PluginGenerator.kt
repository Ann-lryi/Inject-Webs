package com.aho.streambrowser.util

import com.aho.streambrowser.model.NetworkRequest
import com.aho.streambrowser.model.StreamItem
import com.aho.streambrowser.model.StreamType

/** I1: Generate CloudStream3 plugin skeleton from captured session */
object PluginGenerator {

    data class SessionData(
        val streams:   List<StreamItem>,
        val requests:  List<NetworkRequest>,
        val siteUrl:   String,
        val cookies:   String,
        val referer:   String
    )

    /** Generate full extractorLink code from captured streams */
    fun generateExtractorCode(session: SessionData): String {
        val sb = StringBuilder()
        sb.appendLine("// Auto-generated by StreamBrowser v3")
        sb.appendLine("// Site: ${session.siteUrl}")
        sb.appendLine("// Captured: ${session.streams.size} stream(s)")
        sb.appendLine()
        sb.appendLine("override suspend fun loadLinks(")
        sb.appendLine("    data: String, isCasting: Boolean,")
        sb.appendLine("    subtitleCallback: (SubtitleFile) -> Unit,")
        sb.appendLine("    callback: (ExtractorLink) -> Unit")
        sb.appendLine("): Boolean {")
        if (session.streams.isEmpty()) {
            sb.appendLine("    // No streams captured — implement manually")
            sb.appendLine("    return false")
        } else {
            session.streams.take(3).forEachIndexed { i, stream ->
                val type = if (stream.type == StreamType.HLS) "ExtractorLinkType.M3U8" else "ExtractorLinkType.VIDEO"
                sb.appendLine()
                sb.appendLine("    // Stream ${i + 1} [${stream.source}]")
                sb.appendLine("    callback(newExtractorLink(")
                sb.appendLine("        source = name,")
                sb.appendLine("        name   = name,")
                sb.appendLine("        url    = \"${stream.url}\",")
                sb.appendLine("        type   = $type")
                sb.appendLine("    ) {")
                sb.appendLine("        quality = Qualities.P1080.value")
                sb.appendLine("        headers = mapOf(")
                sb.appendLine("            \"User-Agent\" to \"Mozilla/5.0 (Linux; Android 14) AppleWebKit/537.36\",")
                sb.appendLine("            \"Referer\"    to \"${session.referer}\"" +
                    if (session.cookies.isNotBlank()) "," else "")
                if (session.cookies.isNotBlank())
                    sb.appendLine("            \"Cookie\"     to \"${session.cookies.take(200)}\"")
                sb.appendLine("        )")
                sb.appendLine("    })")
            }
            sb.appendLine()
            sb.appendLine("    return true")
        }
        sb.appendLine("}")
        return sb.toString()
    }

