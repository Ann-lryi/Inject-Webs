    /** Reverse-engineer URL pattern from captured requests */
    fun analyzePatterns(requests: List<NetworkRequest>): String {
        val sb = StringBuilder("// URL Patterns found:\n")
        val apiCalls = requests.filter { r ->
            r.url.contains("api") || r.url.contains("json") || r.url.contains("data")
        }.take(10)
        apiCalls.forEach { r ->
            sb.appendLine("// ${r.method} ${r.url.take(100)}")
            if (r.headers.isNotEmpty()) {
                r.headers.filter { (k, _) -> k.lowercase() !in listOf("host", "connection", "accept-encoding") }
                    .forEach { (k, v) -> sb.appendLine("//   $k: ${v.take(60)}") }
            }
        }
        val streamReqs = requests.filter { it.isStream }.take(5)
        if (streamReqs.isNotEmpty()) {
            sb.appendLine("\n// Stream requests:")
            streamReqs.forEach { r -> sb.appendLine("// ${r.url.take(100)}") }
        }
        return sb.toString()
    }
}
